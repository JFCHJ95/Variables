# Operators
var2: int = 5
var3: int = 4

# Equal ==
if var2 == var3:
    print("Equal")

# Not equal !=
if var2 != var3:
    print("Not equal")

# Greater than >
if var2 > var3:
    print("Is greater")

# Less than <
if var2 < var3:
    print("Is less")

# Greater than or equal to >=
if var2 >= var3:
    print("Is greater than or equal to")

# Less than or equal <=
if var2 <= var3:
    print("Is less than or equal to")